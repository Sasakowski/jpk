function Setup(ID) {
	let _ = document.getElementById(ID);
	_ = _.getElementsByTagName("int");

	for (let elem of _) {
		let letters = [];
		if (elem.innerHTML.includes(",")) {
			letters = elem.innerHTML.split(",");
		} else {
			letters.push(elem.innerHTML);
		}

		elem.innerHTML = "";
		for (let l of letters) {
			if (l === "") { continue; }
			elem.innerHTML += `<button onclick = "Input('${l}');"><text>${l}<text></button>`;
		}
	}
}

function InputM(ID) {
	let _ = document.getElementById("multirad " + ID);

	if (_.classList.contains("selected")) {
		_.classList.remove("selected");
	} else {
		_.classList.add("selected");
	}

	let FOUND = LookupMultirads();
	let DOM = document.getElementById("MULTIRAD_OUTPUT");
	DOM.innerHTML = "";

	if (FOUND === null) { DOM.innerHTML = "Empty selection."; return; }
	if (Object.keys(FOUND).length === 0) { DOM.innerHTML = "Found nothing."; return; }

	for (let [k,v] of Object.entries(FOUND)) {
		let STRING = ``;
		for (let _v of v) { STRING += `<button onclick = "Input('${_v}');"><text_l>${_v}<text_l></button>`; }
		DOM.innerHTML += `<flex_columns><text_l>${k}</text_l><space></space>${STRING}</flex_columns>`;
	}
}

function LookupMultirads() {
	// First get which multirads are selected.
	let _ = document.getElementsByClassName("selected");
	if (_.length === 0) { document.getElementById("MULTIRAD_OUTPUT").innerHTML = ""; return null; }
	let SELECTED = [];
	for (let d of _) {
		SELECTED.push(d.id.replace("multirad ", ""));
	}
	let DUP_MIN = SELECTED.length;

	// Second, get the dict-arrays of the selected and then merge them into one.
	let SEL_ARRAYS = [];
	for (let s of SELECTED) {
		SEL_ARRAYS.push(MULTIRADDICT[s]);
	}
	if (SEL_ARRAYS.length === 1) { return SEL_ARRAYS[0]; }

	// Third, combine the dict-arrays into one.
	let COMBINED = {};
	for (let arr of Object.values(SEL_ARRAYS)) {
		for (let [k,v] of Object.entries(arr)) {
			if (!Object.keys(COMBINED).includes(k)) { COMBINED[k] = []; }
			COMBINED[k] = COMBINED[k].concat(v);
		}
	}

	// Fourth, now get out the duplicates, because these are the ones we need.
	let FILTERED = {};
	for (let [k,v] of Object.entries(COMBINED)) {
		let DUPLICATES = [];
		for (let _v of v) {
			if (DUPLICATES.includes(_v)) { continue; }
			let _ = v.filter(x => x === _v);
			if (_.length > DUP_MIN-1) { DUPLICATES.push(_v); }
		}
		if (DUPLICATES.length > 0) { FILTERED[k] = DUPLICATES; }
	}

	return FILTERED;
}

